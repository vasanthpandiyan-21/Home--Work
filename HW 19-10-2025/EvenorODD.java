class EvenorODD

{
	public static void main(String[]args)
	{
		int num = 26;
        if (num%2==0)
		{
			System.out.println("num "+" the number is even ");
		}
		else if (num%2==1)
		{
			System.out.println("num "+" the number is odd ");
		}
        else
		{
            System.out.println("wrong number");
		}	
	}		
}